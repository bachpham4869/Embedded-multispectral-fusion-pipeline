def build_alignment_diagnostics(paired_eval_dir: Path) -> list[dict[str, Any]]:
    manifest_rows = _read_csv(paired_eval_dir / "strict_paired_manifest.csv")
    inventory_rows = _read_csv(paired_eval_dir / "paired_data_inventory.csv")
    captured_flag = _captured_flag(paired_eval_dir)
    skews = [_safe_float(row.get("pairing_gap_ms")) for row in manifest_rows]
    skews = [s for s in skews if s is not None]
    p95 = _percentile(skews, 95)
    mean = sum(skews) / len(skews) if skews else None
    rows = [
        {
            "diagnostic": "frame_skew_ms",
            "status": "measured" if skews else "not_measured",
            "n": len(skews),
            "mean": _fmt(mean),
            "measured_p95": _fmt(p95),
            "estimated_p95": "",
            "p95_source": "measured_from_timestamps_csv" if skews else "",
            "result": f"mean={_fmt(mean)}; p95={_fmt(p95)}" if skews else "not measured",
            "pairing_tier": _pairing_distribution(manifest_rows),
            "captured_runtime_fusion_available": captured_flag,
            "alignment_metadata": "not_measured",
            "caveat": "Frame skew supports capture pairing only; it does not prove spatial alignment quality.",
            "thesis_wording": "strong" if skews else "not measured",
        }
    ]
    dims: dict[str, str] = {}
    for row in inventory_rows:
        path = row.get("path", "")
        width = row.get("width", "")
        height = row.get("height", "")
        if not width or not height:
            continue
        if "imx" in path:
            dims["nir"] = f"{width}x{height}"
        elif "thermal" in path and path.endswith((".mp4", ".avi")):
            dims["thermal"] = f"{width}x{height}"
    mismatch = dims.get("nir") and dims.get("thermal") and dims.get("nir") != dims.get("thermal")
    rows.append(
        {
            "diagnostic": "resolution_mismatch",
            "status": "measured" if dims else "not_measured",
            "n": len(dims),
            "mean": "",
            "measured_p95": "",
            "estimated_p95": "",
            "p95_source": "",
            "result": f"{dims.get('nir', 'unknown')} vs {dims.get('thermal', 'unknown')}" if dims else "not measured",
            "pairing_tier": _pairing_distribution(manifest_rows),
            "captured_runtime_fusion_available": captured_flag,
            "alignment_metadata": "not_measured",
            "caveat": "Resolution mismatch requires explicit resize/warp policy; do not infer spatial alignment quality." if mismatch else "Resolution metadata only.",
            "thesis_wording": "caveated" if dims else "not measured",
        }
    )
    has_homography = any((row.get("homography_path") or "").strip() for row in manifest_rows)
    rows.append(
        {
            "diagnostic": "homography_alignment_metadata",
            "status": "measured" if has_homography else "not_measured",
            "n": sum(1 for row in manifest_rows if (row.get("homography_path") or "").strip()),
            "mean": "",
            "measured_p95": "",
            "estimated_p95": "",
            "p95_source": "",
            "result": "homography path present" if has_homography else "not measured",
            "pairing_tier": _pairing_distribution(manifest_rows),
            "captured_runtime_fusion_available": captured_flag,
            "alignment_metadata": "measured" if has_homography else "not_measured",
            "caveat": "Do not claim alignment quality without homography/alignment metadata and visual verification.",
            "thesis_wording": "caveated" if has_homography else "not measured",
        }
    )
    return rows


