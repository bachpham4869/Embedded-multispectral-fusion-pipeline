def _resolve(root: Path, path: Path) -> Path:
    return path if path.is_absolute() else root / path


def _rel(repo_root: Path, path: Path) -> str:
    try:
        return str(path.resolve().relative_to(repo_root.resolve()))
    except ValueError:
        return str(path)


def _git_commit(repo_root: Path) -> str:
    try:
        return subprocess.check_output(["git", "rev-parse", "HEAD"], cwd=repo_root, text=True).strip()
    except Exception:
        return "unknown"


def _now_iso() -> str:
    return dt.datetime.now(dt.timezone.utc).isoformat(timespec="seconds")


def _read_csv(path: Path) -> list[dict[str, str]]:
    if not path.exists():
        return []
    with path.open(newline="", encoding="utf-8") as fh:
        return list(csv.DictReader(fh))


def _write_csv(path: Path, rows: Iterable[dict[str, Any]], fieldnames: list[str] | None = None) -> None:
    row_list = list(rows)
    if fieldnames is None:
        fieldnames = []
        for row in row_list:
            for key in row:
                if key not in fieldnames:
                    fieldnames.append(key)
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as fh:
        writer = csv.DictWriter(fh, fieldnames=fieldnames)
        writer.writeheader()
        for row in row_list:
            writer.writerow({key: row.get(key, "") for key in fieldnames})


def _safe_float(value: Any) -> float | None:
    try:
        result = float(value)
    except (TypeError, ValueError):
        return None
    return result if math.isfinite(result) else None


def _fmt(value: Any) -> str:
    if value is None:
        return ""
    if isinstance(value, int):
        return str(value)
    if isinstance(value, float):
        if not math.isfinite(value):
            return ""
        return f"{value:.6f}"
    return str(value)


def _md_escape(value: Any) -> str:
    text = _fmt(value)
    return text.replace("|", "\\|").replace("\n", " ")


def _markdown_table(rows: Iterable[dict[str, Any]], columns: list[str]) -> str:
    row_list = list(rows)
    lines = [
        "| " + " | ".join(columns) + " |",
        "| " + " | ".join("---" for _ in columns) + " |",
    ]
    for row in row_list:
        lines.append("| " + " | ".join(_md_escape(row.get(col, "")) for col in columns) + " |")
    return "\n".join(lines) + "\n"


def _write_markdown(path: Path, title: str, body: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(f"# {title}\n\n{body.rstrip()}\n", encoding="utf-8")


def _counter_summary(values: Iterable[str]) -> str:
    counts = Counter(v or "unknown" for v in values)
    return ", ".join(f"{key}:{counts[key]}" for key in sorted(counts))


def _unique_summary(values: Iterable[str]) -> str:
    vals = sorted({v for v in values if v})
    return ",".join(vals)


def _pairing_distribution(manifest_rows: list[dict[str, str]]) -> str:
    return _counter_summary(row.get("pairing_tier", "unknown") for row in manifest_rows)


def _strict_count(manifest_rows: list[dict[str, str]]) -> int:
    return sum(1 for row in manifest_rows if row.get("pairing_tier") in PAIRING_STRICT_TIERS)


def captured_runtime_fusion_available(manifest_path: Path, fusion_metrics_path: Path | None = None) -> bool:
    manifest_rows = _read_csv(manifest_path)
    if any((row.get("fusion_output_path") or "").strip() for row in manifest_rows):
        return True
    if fusion_metrics_path is not None:
        fusion_rows = _read_csv(fusion_metrics_path)
        if any(row.get("fusion_source") == CAPTURED_FUSION_SOURCE for row in fusion_rows):
            return True
    return False


def _captured_flag(paired_eval_dir: Path) -> str:
    return (
        "true"
        if captured_runtime_fusion_available(
            paired_eval_dir / "strict_paired_manifest.csv",
            paired_eval_dir / "fusion_quality_metrics.csv",
        )
        else "false"
    )


def _metric_summary(rows: list[dict[str, str]], *, algorithm: str, metric: str) -> dict[str, Any] | None:
    selected = [row for row in rows if row.get("algorithm") == algorithm and row.get("metric") == metric]
    if not selected:
        return None
    summary = summarize_metric_rows(selected, value_key="value", baseline_key="baseline_value", bootstrap_iterations=200)
    summary["n"] = int(summary.get("n") or 0)
    summary["pairing_tier"] = _unique_summary(row.get("pairing_tier", "") for row in selected)
    summary["metric_tier"] = _unique_summary(row.get("metric_tier", "") for row in selected)
    summary["evidence_label"] = _counter_summary(row.get("evidence_label", "") for row in selected)
    summary["input_data_type"] = _unique_summary(row.get("input_data_type", "") for row in selected)
    summary["thermal_modality"] = _unique_summary(row.get("thermal_modality", "") for row in selected)
    summary["fusion_source"] = _unique_summary(row.get("fusion_source", "") for row in selected)
    summary["baseline_algorithm"] = _unique_summary(row.get("baseline_algorithm", "") for row in selected)
    return summary


def _result_text(summary: dict[str, Any] | None, *, include_mean: bool = True) -> str:
    if not summary or int(summary.get("n") or 0) == 0:
        return "not measured"
    parts = [f"n={summary['n']}"]
    if include_mean:
        parts.append(f"mean={_fmt(summary.get('mean'))}")
    if summary.get("delta_current_minus_baseline") is not None:
        parts.append(f"delta={_fmt(summary.get('delta_current_minus_baseline'))}")
    if summary.get("win_rate_current_vs_baseline") is not None:
        parts.append(f"win_rate={_fmt(summary.get('win_rate_current_vs_baseline'))}")
    return "; ".join(parts)


