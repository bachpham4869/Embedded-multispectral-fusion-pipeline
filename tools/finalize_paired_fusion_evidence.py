def _replace_report_patch_section(path: Path, heading: str, section_body: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    old = path.read_text(encoding="utf-8") if path.exists() else ""
    lines = old.splitlines()
    start = None
    for idx, line in enumerate(lines):
        if line.strip() == heading:
            start = idx
            break
    new_section = [heading, "", *section_body.splitlines(), ""]
    if start is None:
        if old and not old.endswith("\n"):
            old += "\n"
        path.write_text(old + "\n".join(new_section).rstrip() + "\n", encoding="utf-8")
        return
    end = len(lines)
    for idx in range(start + 1, len(lines)):
        if lines[idx].startswith("## "):
            end = idx
            break
    updated = lines[:start] + new_section + lines[end:]
    path.write_text("\n".join(updated).rstrip() + "\n", encoding="utf-8")


def write_final_outputs(*, repo_root: Path, paired_eval_dir: Path, max_visual_review_samples: int = 12) -> list[Path]:
    repo_root = repo_root.resolve()
    paired_eval_dir = _resolve(repo_root, paired_eval_dir)
    tables_dir = repo_root / "docs" / "tables" / "fusion"
    final_rows = build_final_result_rows(paired_eval_dir)
    readiness_rows = build_evidence_readiness_rows(paired_eval_dir)
    visual_rows = build_agent_visual_bucket_review(paired_eval_dir, repo_root, max_visual_review_samples)
    _write_csv(paired_eval_dir / "agent_visual_bucket_review.csv", visual_rows)

    per_bucket_rows = build_per_bucket_report_rows(paired_eval_dir)
    failure_rows = _failure_limitation_rows(paired_eval_dir)
    alignment_rows = build_alignment_diagnostics(paired_eval_dir)
    visual_summary = _visual_review_summary(visual_rows)

    _write_markdown(tables_dir / "fusion_result_summary_for_report.md", "Fusion Result Summary For Report", _markdown_table(final_rows, FINAL_SUMMARY_COLUMNS))
    _write_markdown(
        tables_dir / "fusion_failure_and_limitation_summary.md",
        "Fusion Failure And Limitation Summary",
        _markdown_table(
            failure_rows,
            [
                "failure_type",
                "algorithm",
                "n",
                "pairing_tier",
                "fusion_source",
                "captured_runtime_fusion_available",
                "evidence_tier",
                "severity_trigger",
                "report_caveat",
                "thesis_wording",
            ],
        ),
    )
    _write_markdown(tables_dir / "per_bucket_report_summary.md", "Per-Bucket Report Summary", _markdown_table(per_bucket_rows, PER_BUCKET_COLUMNS))
    _write_markdown(tables_dir / "per_bucket_processing_eval.md", "Per-Bucket Processing Evaluation", _markdown_table(per_bucket_rows, PER_BUCKET_COLUMNS))
    missing = [row for row in per_bucket_rows if row.get("bucket_evidence_status") == "not_measured"]
    _write_markdown(tables_dir / "per_bucket_missing_evidence.md", "Per-Bucket Missing Evidence", _markdown_table(missing, PER_BUCKET_COLUMNS))
    _write_markdown(
        tables_dir / "paired_capture_alignment_diagnostics.md",
        "Paired Capture Alignment Diagnostics",
        _markdown_table(alignment_rows, ALIGNMENT_COLUMNS),
    )
    _write_markdown(
        tables_dir / "agent_visual_bucket_review_summary.md",
        "Agent Visual Bucket Review Summary",
        _markdown_table(visual_summary, ["visual_condition", "possible_bucket", "bucket_source", "n", "label_source", "caveat"]),
    )
    _write_metric_summary_tables(repo_root, paired_eval_dir)
    _write_text_docs(repo_root, paired_eval_dir, final_rows, readiness_rows)

    manifest = {
        "timestamp_iso": _now_iso(),
        "git_commit": _git_commit(repo_root),
        "command": " ".join(sys.argv),
        "inputs": [
            _rel(repo_root, paired_eval_dir / "strict_paired_manifest.csv"),
            _rel(repo_root, paired_eval_dir / "nir_quality_metrics.csv"),
            _rel(repo_root, paired_eval_dir / "thermal_quality_metrics.csv"),
            _rel(repo_root, paired_eval_dir / "fusion_quality_metrics.csv"),
            _rel(repo_root, paired_eval_dir / "strict_paired_failure_cases.csv"),
        ],
        "config": {
            "captured_runtime_fusion_available": _captured_flag(paired_eval_dir),
            "thermal_modality": THERMAL_DISPLAY_MODALITY,
            "fusion_source_for_generated_modes": GENERATED_FUSION_SOURCE,
            "max_visual_review_samples": max_visual_review_samples,
        },
        "metric_definitions_version": "fusion_eval_metrics_v1",
    }
    (paired_eval_dir / "paired_fusion_final_evidence_run_manifest.json").write_text(
        json.dumps(manifest, indent=2, sort_keys=True),
        encoding="utf-8",
    )
    return [
        repo_root / "docs" / "fusion" / "FUSION_EVIDENCE_READINESS.md",
        tables_dir / "evidence_readiness_matrix.md",
        repo_root / "docs" / "paired" / "PAIRED_DATA_FUSION_EVALUATION_SUMMARY.md",
        tables_dir / "fusion_result_summary_for_report.md",
        tables_dir / "fusion_failure_and_limitation_summary.md",
        repo_root / "docs" / "fusion" / "REPORT_SECTIONS_FUSION_DRAFT.md",
        paired_eval_dir / "agent_visual_bucket_review.csv",
        paired_eval_dir / "paired_fusion_final_evidence_run_manifest.json",
    ]


def main() -> int:
    args = parse_args()
    repo_root = args.repo_root.resolve()
    paired_eval_dir = _resolve(repo_root, args.paired_eval_dir)
    planned = [
        repo_root / "docs" / "fusion" / "FUSION_EVIDENCE_READINESS.md",
        repo_root / "docs" / "tables" / "fusion" / "evidence_readiness_matrix.md",
        repo_root / "docs" / "paired" / "PAIRED_DATA_FUSION_EVALUATION_SUMMARY.md",
        repo_root / "docs" / "tables" / "fusion" / "fusion_result_summary_for_report.md",
        repo_root / "docs" / "tables" / "fusion" / "fusion_failure_and_limitation_summary.md",
        repo_root / "docs" / "fusion" / "REPORT_SECTIONS_FUSION_DRAFT.md",
        paired_eval_dir / "agent_visual_bucket_review.csv",
    ]
    if args.dry_run:
        print("Would write:")
        for path in planned:
            print(path)
        return 0
    written = write_final_outputs(
        repo_root=repo_root,
        paired_eval_dir=paired_eval_dir,
        max_visual_review_samples=args.max_visual_review_samples,
    )
    print(f"Finalized paired fusion evidence from {paired_eval_dir}")
    for path in written:
        print(path)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
